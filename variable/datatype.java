class datatype
{
    public static void main(String args[])
    {
        int x=10;
        float f=34.13f;
        double d=34.231;
        char c='a';
        boolean b = true;
        long l = 1000;
        short s =100;
        String g="venu";
        System.out.println("the main starts here");
        System.out.println("the value of integer x : "+x);
        System.out.println("the value of float f :"+f);
        System.out.println("the value of double d:"+d);
        System.out.println("the value of character c : "+c);
        System.out.println("the value of boolean b :"+b);
        System.out.println("the value of long l :"+l);
        System.out.println("the value of short s :"+s);
        System.out.println("the value of string is :"+g);
        System.out.println("main ends here");
    }
}